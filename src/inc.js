module.exports = {
  /**
   * Adds 1 to `x`.
   *
   * @example
   * ```javascript
   * inc(41);
   * //=> 42
   * ```
   *
   * @public
   * @param {number} x Any number
   * @return {number}
   */
  inc: x => x + 1
};
