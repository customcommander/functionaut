/**
 * @namespace
 * @alias ROOT
 */
module.exports = {
  /**
   * Takes 1 away from `x`.
   *
   * @example
   * ```javascript
   * dec(43);
   * //=> 42
   * ```
   *
   * @public
   * @param {number} x Any number
   * @return {number}
   */
  dec: x => x - 1
};
